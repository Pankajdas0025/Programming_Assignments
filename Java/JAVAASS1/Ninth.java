//  Write a program that will illustrate the concept of default
// constructor.



public class Ninth {
    // Default constructor
    public Ninth() {
        System.out.println("Default constructor called.");
    }

    public static void main(String[] args) {
        // Creating an object of Ninth class
        Ninth obj = new Ninth();
        System.out.println("Object created successfully.");
    }
}